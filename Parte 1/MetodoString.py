cadena = "EstOY utiLizaNdo lOS mEtoDoS dE pYtHoN"

print(cadena.lower()) # Pone la cadena en minuscula
print(cadena.upper()) # Pone la cadena en mayuscula
print(cadena.capitalize()) # Pone la primera mayuscula en la cadena
print(cadena.title()) # Pone la mayuscula en cada palabra
print(cadena.swapcase()) # invierte mayusculas en minusculas y viceversa